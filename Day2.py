# Primitive data types
# len() on int does not work

# subscripting
# print the first character of a string
print("Hello"[0])

# print the last character of a string
print("Hello"[4])
print("Hello"[-1])  # -1 = last char of the string

# Concatenation
print("123" + "456")

# Integer = Whole number
print(123+456)

# two write large integers you can use _
print(123_456)  # 123,456

# Float = floating point number
# the decimal point can float anywhere in the number
# 12.34 1.234 123.4
print(1.2345)

# Boolean
print(True)
print(False)
